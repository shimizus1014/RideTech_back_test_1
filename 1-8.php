<?php
/**
 * セクション1 テスト 1-8：多次元配列の行表示と合計
 *
 * やること
 *  1) $mat = [[1,2,3],[4,5,6],[7,8,9]]
 *  2) 各行を "1 2 3" のようにスペース区切りで表示
 *  3) 各行の合計を "row0: sum=6" のように表示（行番号は 0,1,2…）
 *
 * 実行方法
 *  - php 1-8.php
 */
// ここから実装してください

$mat = [[1,2,3],[4,5,6],[7,8,9]];
foreach ($mat as $index => $row) {
    echo implode(" ", $row) . PHP_EOL;
    echo "row" . $index . ": sum=" . array_sum($row) . PHP_EOL;
}
